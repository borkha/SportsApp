package ModelClass;

public class Groups
{
    public String name;
    public String GroupName;

    public Groups(String name) {
        this.name = name;
    }

    public Groups() {
    }


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getGroupName() {
        return GroupName;
    }

    public void setGroupName(String groupName) {
        GroupName = groupName;
    }
}
